public class PTra02_01 {
	public static void main(String[] args) {

		// ★ 20を5で割った値を出力してください
		int a;
		a = 20;
		System.out.println(a / 5);
		// ★ 126に13を掛けた値を出力してください
		int b;
		b = 126;
		System.out.println(b * 13);
		// ★ 97を6で割ったときの余りを出力してください
		int c;
		c = 97;
		System.out.println(c % 6);
	}
}
